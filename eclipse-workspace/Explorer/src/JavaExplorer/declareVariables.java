package JavaExplorer;

public class declareVariables {// opening curly brace

	String model;// declared variables
	int price;
	char quality;
	boolean madeInUsa;

	public void computer() { // declaration of constructor
		System.out.println("we are describing computer here");
	}

	public void computerConfiguration() {// method implementation
		System.out.println("computer configuration is; " + model + "  " + price + "  " + quality + " " + madeInUsa);
	}

}// closing of curly brace
